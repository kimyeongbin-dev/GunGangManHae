-- 0006_pick_method.sql
-- 스네이크 방향(1팀부터 / 16팀부터) 계산에서 '랜덤 배치로 채운 티어'를 빼기 위한 표식.
--
-- ★ 왜 필요한가
--   지그재그 방향은 "이 티어가 몇 번째로 뽑는 티어인가"로 정해지고, 그 순번은
--   '이미 다 찬 티어 수'로 센다. 그런데 랜덤 배치로 통째로 채운 티어까지 순번에 끼면
--   실제로 지명한 순서와 어긋난다.
--     예) 1티어를 랜덤으로 채우고 2티어부터 직접 뽑기 시작 → 2티어가 '두 번째'로 잡혀 16팀부터 시작.
--         하지만 직접 뽑은 건 2티어가 처음이므로 1팀부터가 맞다.
--   participants만 봐서는 어떻게 채워졌는지 알 수 없으므로, 배정 방식을 행에 남긴다.
--
--   · false(기본) = 진행자가 한 명씩 직접 지명 → 순번에 포함
--   · true        = '티어 랜덤 배치'로 한 번에 배치 → 순번에서 제외
--   팀장은 스네이크 픽이 아니므로 항상 false로 둔다.
-- 선행: 0001 ~ 0005 실행 완료. (여러 번 실행해도 안전)
-- =====================================================================

alter table public.participants
  add column if not exists assigned_randomly boolean not null default false;
